package edu.ewubd.foodblog;

public class Posts {
    String key="";
    String title="";
    String desc="";
    String img="";
    String userMail="";
    String dateTime="";


    public Posts(String key, String title, String desc,String img, String userMail, String dateTime) {
        this.key = key;
        this.title = title;
        this.desc = desc;
        this.img = img;
        this.userMail = userMail;
        this.dateTime = dateTime;
    }
}

